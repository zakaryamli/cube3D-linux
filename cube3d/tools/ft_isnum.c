#include "../cub3d.h"

int	ft_isnum(char *str)
{
	int	i;

	i = 0;
	if (str[i] && str[i] == '+')
		i++;
	if (!str[i] || !(str[i] >= '0' && str[i] <= '9'))
		return (0);
	i++;
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{
		if (!(str[i] >= '0' && str[i] <= '9'))
			return (0);
		i++;
	}
	return (1);
}
