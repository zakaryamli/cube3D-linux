#include "../cub3d.h"

int	stop_here2(char *str)
{
	int	i;

	i = 0;
	while (str[i] && ft_is_space(str[i]))
		i++;
	if (str[i] && ft_strchr("01", str[i]))
		return (0);
	return (1);
}
